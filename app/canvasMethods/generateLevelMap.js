export default function generateBrickArray(totalRows, totalColumns, brickWidth, brickHeight, level) {
  const arr = [];
  for (let row = 0; row < totalRows; row += 1) {
    for (let col = 0; col < totalColumns; col += 1) {
      const color = generateColor();
      const x = col * brickWidth;
      const y = row * brickHeight;
      const index = col + (totalColumns * row);
      const active = levels[level][index];
      const brick = {
        x,
        y,
        color,
        active,
      };
      arr.push(brick);
    }
  }
  return arr;
}
